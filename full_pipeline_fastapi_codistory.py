def split_arabic_scenes(arabic_translation: str):
    # 1. Remove all newline characters
    clean_text = arabic_translation.replace("\n", "").strip()

    # 2. Split by "Ø§Ù„Ù…Ø´Ù‡Ø¯" keyword
    parts = clean_text.split("Ø§Ù„Ù…Ø´Ù‡Ø¯")

    # 3. Reattach "Ø§Ù„Ù…Ø´Ù‡Ø¯" and remove empty results
    arabic_scenes = [
        f"Ø§Ù„Ù…Ø´Ù‡Ø¯{part.strip()}" for part in parts if part.strip()
    ]

    return arabic_scenes

